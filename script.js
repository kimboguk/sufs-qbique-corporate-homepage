let currentLang = (function detectLang() {
  const path = window.location.pathname;
  if (path.includes('/en/') || path.endsWith('/en')) return 'en';
  return 'ko';
})();

function applyLang(lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (i18n[lang][key]) el.innerHTML = i18n[lang][key];
  });
  document.documentElement.lang = lang;
  currentLang = lang;
}

(function initTheme(){
  const root = document.documentElement;
  const saved = localStorage.getItem('theme');
  const prefersLight = window.matchMedia('(prefers-color-scheme: light)').matches;
  const current = saved || (prefersLight ? 'light' : 'dark');
  if (current === 'light') root.setAttribute('data-theme', 'light');
})();

document.addEventListener('DOMContentLoaded', () => {
  const year = document.getElementById('year');
  if (year) year.textContent = new Date().getFullYear();

  applyLang(currentLang);

  const langBtn = document.getElementById('langToggle');
  langBtn?.addEventListener('click', () => {
    const path = window.location.pathname;
    const isEn = path.includes('/en/') || path.endsWith('/en');
    if (currentLang === 'ko') {
      // ko → en: /en/ 경로로 이동
      const base = path.replace(/\/?(index\.html)?$/, '');
      window.location.href = base + '/en/';
    } else {
      // en → ko: 상위 경로로 이동
      const base = path.replace(/\/en\/?(index\.html)?$/, '') || '/';
      window.location.href = base + '/';
    }
  });

  const toggle = document.getElementById('themeToggle');
  toggle?.addEventListener('click', () => {
    const light = document.documentElement.getAttribute('data-theme') === 'light';
    document.documentElement.setAttribute('data-theme', light ? '' : 'light');
    localStorage.setItem('theme', light ? 'dark' : 'light');
  });

  // Mobile nav
  const btn = document.querySelector('.nav-toggle');
  const list = document.getElementById('navMenu');
  btn?.addEventListener('click', () => {
    const open = list?.classList.toggle('open');
    btn.setAttribute('aria-expanded', open ? 'true' : 'false');
  });

  // Smooth scroll
  document.querySelectorAll('a[href^="#"]').forEach(a => {
    a.addEventListener('click', e => {
      const id = a.getAttribute('href')?.slice(1);
      const target = id ? document.getElementById(id) : null;
      if (target) {
        e.preventDefault();
        target.scrollIntoView({ behavior: 'smooth', block: 'start' });
        list?.classList.remove('open');
      }
    });
  });

  // Demo form handler
  const form = document.querySelector('.contact-form');
  const note = document.getElementById('formNote');
  form?.addEventListener('submit', (e) => {
    e.preventDefault();
    if (!note) return;
    note.textContent = currentLang === 'ko' 
      ? '감사합니다! 담당자가 확인 후 연락드리겠습니다.' 
      : 'Thank you! We will contact you shortly.';
    setTimeout(() => note.textContent = '', 5000);
  });
});
